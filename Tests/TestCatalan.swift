import XCTest

final class TestCatalan: XCTestCase {
    
    func test_catalan() {
        let catalan = Languages().catalan
        
        XCTAssertEqual(catalan.read(-1), "menys u")
        XCTAssertEqual(catalan.read("-1"), "menys u")
        XCTAssertEqual(catalan.read("-1.5"), "menys u coma cinc")
        XCTAssertEqual(catalan.read("-1.51"), "menys u coma cinc u")
        XCTAssertEqual(catalan.read("-0.7"), "menys zero coma set")
        XCTAssertEqual(catalan.read("-1.5"), "menys u coma cinc")
        XCTAssertEqual(catalan.read("1.5"), "u coma cinc")
        XCTAssertEqual(catalan.read(1), "u")
        XCTAssertEqual(catalan.read(2), "dos")
        XCTAssertEqual(catalan.read(3), "tres")
        XCTAssertEqual(catalan.read(4), "quatre")
        XCTAssertEqual(catalan.read(5), "cinc")
        XCTAssertEqual(catalan.read(6), "sis")
        XCTAssertEqual(catalan.read(7), "set")
        XCTAssertEqual(catalan.read(8), "vuit")
        XCTAssertEqual(catalan.read(9), "nou")
        XCTAssertEqual(catalan.read(10), "deu")
        XCTAssertEqual(catalan.read(11), "onze")
        XCTAssertEqual(catalan.read(12), "dotze")
        XCTAssertEqual(catalan.read(13), "tretze")
        XCTAssertEqual(catalan.read(14), "catorze")
        XCTAssertEqual(catalan.read(15), "quinze")
        XCTAssertEqual(catalan.read(16), "setze")
        XCTAssertEqual(catalan.read(17), "disset")
        XCTAssertEqual(catalan.read(18), "divuit")
        XCTAssertEqual(catalan.read(19), "dinou")
        XCTAssertEqual(catalan.read(20), "vint")
        XCTAssertEqual(catalan.read(21), "vint-i-u")
        XCTAssertEqual(catalan.read(22), "vint-i-dos")
        XCTAssertEqual(catalan.read(23), "vint-i-tres")
        XCTAssertEqual(catalan.read(24), "vint-i-quatre")
        XCTAssertEqual(catalan.read(25), "vint-i-cinc")
        XCTAssertEqual(catalan.read(26), "vint-i-sis")
        XCTAssertEqual(catalan.read(27), "vint-i-set")
        XCTAssertEqual(catalan.read(28), "vint-i-vuit")
        XCTAssertEqual(catalan.read(29), "vint-i-nou")
        XCTAssertEqual(catalan.read(30), "trenta")
        XCTAssertEqual(catalan.read(31), "trenta-u")
        XCTAssertEqual(catalan.read(32), "trenta-dos")
        XCTAssertEqual(catalan.read(33), "trenta-tres")
        XCTAssertEqual(catalan.read(34), "trenta-quatre")
        XCTAssertEqual(catalan.read(35), "trenta-cinc")
        XCTAssertEqual(catalan.read(36), "trenta-sis")
        XCTAssertEqual(catalan.read(37), "trenta-set")
        XCTAssertEqual(catalan.read(38), "trenta-vuit")
        XCTAssertEqual(catalan.read(39), "trenta-nou")
        XCTAssertEqual(catalan.read(40), "quaranta")
        XCTAssertEqual(catalan.read(41), "quaranta-u")
        XCTAssertEqual(catalan.read(50), "cinquanta")
        XCTAssertEqual(catalan.read(60), "seixanta")
        XCTAssertEqual(catalan.read(70), "setanta")
        XCTAssertEqual(catalan.read(80), "vuitanta")
        XCTAssertEqual(catalan.read(90), "noranta")
        XCTAssertEqual(catalan.read(100), "cent")
        XCTAssertEqual(catalan.read(101), "cent u")
        XCTAssertEqual(catalan.read(102), "cent dos")
        XCTAssertEqual(catalan.read(103), "cent tres")
        XCTAssertEqual(catalan.read(104), "cent quatre")
        XCTAssertEqual(catalan.read(105), "cent cinc")
        XCTAssertEqual(catalan.read(144), "cent quaranta-quatre")
        XCTAssertEqual(catalan.read(200), "dos-cents")
        XCTAssertEqual(catalan.read(300), "tres-cents")
        XCTAssertEqual(catalan.read(400), "quatre-cents")
        XCTAssertEqual(catalan.read(500), "cinc-cents")
        XCTAssertEqual(catalan.read(600), "sis-cents")
        XCTAssertEqual(catalan.read(700), "set-cents")
        XCTAssertEqual(catalan.read(800), "vuit-cents")
        XCTAssertEqual(catalan.read(900), "nou-cents")
        XCTAssertEqual(catalan.read(1_000), "mil")
        XCTAssertEqual(catalan.read(2_000), "dos mil")
        XCTAssertEqual(catalan.read(3_000), "tres mil")
        XCTAssertEqual(catalan.read(3_001), "tres mil u")
        XCTAssertEqual(catalan.read(6_000), "sis mil")
        XCTAssertEqual(catalan.read(6_001), "sis mil u")
        XCTAssertEqual(catalan.read(10_000), "deu mil")
        XCTAssertEqual(catalan.read(11_001), "onze mil u")
        XCTAssertEqual(catalan.read(12_895), "dotze mil vuit-cents noranta-cinc")
        XCTAssertEqual(catalan.read(12_786), "dotze mil set-cents vuitanta-sis")
        XCTAssertEqual(catalan.read(70_000), "setanta mil")
        XCTAssertEqual(catalan.read(187_372), "cent vuitanta-set mil tres-cents setanta-dos")
        XCTAssertEqual(catalan.read(200_000), "dos-cents mil")
        XCTAssertEqual(catalan.read(280_065), "dos-cents vuitanta mil seixanta-cinc")
        XCTAssertEqual(catalan.read(706_210), "set-cents sis mil dos-cents deu")
        XCTAssertEqual(catalan.read(100_000), "cent mil")
        XCTAssertEqual(catalan.read(1_000_000), "un mili贸")
        XCTAssertEqual(catalan.read(1_000_050), "un mili贸 cinquanta")
        XCTAssertEqual(catalan.read(3_000_100), "tres milions cent")
        XCTAssertEqual(catalan.read(10_000_000), "deu milions")
        XCTAssertEqual(catalan.read(40_000_000), "quaranta milions")
        XCTAssertEqual(catalan.read(100_000_000), "cent milions")
        XCTAssertEqual(catalan.read(500_000_000), "cinc-cents milions")
        XCTAssertEqual(catalan.read(1_000_000_000), "mil milions")
        XCTAssertEqual(catalan.read(1_000_000_200), "mil milions dos-cents")
        XCTAssertEqual(catalan.read(10_000_000_000), "deu mil milions")
        XCTAssertEqual(catalan.read(20_000_000_700), "vint mil milions set-cents")
        XCTAssertEqual(catalan.read(100_000_000_000), "cent mil milions")
        XCTAssertEqual(catalan.read(400_000_000_000), "quatre-cents mil milions")
        XCTAssertEqual(catalan.read(1_000_000_000_000), "un bili贸")
        XCTAssertEqual(catalan.read(999_000_000_000_000), "nou-cents noranta-nou bilions")

        XCTAssertEqual(catalan.read(-1), "menys u")
        XCTAssertEqual(catalan.read(-74), "menys setanta-quatre")
        XCTAssertEqual(catalan.read(-1_000_000), "menys un mili贸")
    }
}
