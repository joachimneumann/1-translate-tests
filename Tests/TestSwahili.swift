        XCTAssertEqual(swahili.read(15), "kumi na tano")
        XCTAssertEqual(swahili.read(26), "ishirini na sita")
        XCTAssertEqual(swahili.read(29), "ishirini na tisa")
        XCTAssertEqual(swahili.read(38), "thelathini na nane")
        XCTAssertEqual(swahili.read(47), "arobaini na saba")
        XCTAssertEqual(swahili.read(55), "hamsini na tano")
        XCTAssertEqual(swahili.read(61), "sitini na moja")
        XCTAssertEqual(swahili.read(72), "sabini na mbili")
        XCTAssertEqual(swahili.read(88), "themanini na nane")
        XCTAssertEqual(swahili.read(92), "tisini na mbili")

        XCTAssertEqual(swahili.read(544), "mia tano, arobaini na nne")
        XCTAssertEqual(swahili.read(642), "mia sita, arobaini na mbili")
        XCTAssertEqual(swahili.read(1_220), "elfu moja, mia mbili, na ishirini")
        XCTAssertEqual(swahili.read(6_885), "elfu sita, mia nane, themanini na tano")
        XCTAssertEqual(swahili.read(8_132), "elfu nane, mia moja, thelathini na mbili")
        XCTAssertEqual(swahili.read(34_899), "elfu thelathini na nne, mia nane, tisini na tisa")
        XCTAssertEqual(swahili.read(60_007), "elfu sitini, na saba")
        XCTAssertEqual(swahili.read(67_000), "elfu sitini na saba")
        XCTAssertEqual(swahili.read(78_000), "elfu sabini na nane")
        XCTAssertEqual(swahili.read(360_009), "laki tatu, elfu sitini, na tisa")
        XCTAssertEqual(swahili.read(555_978), "laki tano, elfu hamsini na tano, mia tisa, sabini na nane")
        XCTAssertEqual(swahili.read(908_695), "laki tisa, elfu nane, mia sita, tisini na tano")
        XCTAssertEqual(swahili.read(3_710_001), "milioni tatu, laki saba, elfu kumi, na moja")
        XCTAssertEqual(swahili.read(7_795_794), "milioni saba, laki saba, elfu tisini na tano, mia saba, tisini na nne")
        XCTAssertEqual(swahili.read(9_437_450), "milioni tisa, laki nne, elfu thelathini na saba, mia nne, na hamsini")
        XCTAssertEqual(swahili.read(11_000_004), "milioni kumi na moja, na nne")
                                                        
        XCTAssertEqual(swahili.read(-1), "menos moja")
        XCTAssertEqual(swahili.read(-75), "menos sabini na tano")
        XCTAssertEqual(swahili.read(-1_000_000), "menos milioni moja")
        XCTAssertEqual(swahili.read("3.1415926"), "tatu kaj moja nne moja tano tisa mbili sita")
        XCTAssertEqual(swahili.read("-3.1415926"), "menos tatu kaj moja nne moja tano tisa mbili sita")
        XCTAssertEqual(swahili.read("3.4e16"), "tatu kaj nne EE kumi na sita")
        XCTAssertEqual(swahili.read("3.5e26"), "tatu kaj tano EE ishirini na sita")
    }
}
